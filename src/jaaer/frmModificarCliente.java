    private void listaCategoria (){
        cboIdCategoria.removeAllItems();
        CategoriaDao catDao = new CategoriaDao();
        try {
            lstModCategoria = catDao.listarCategoria();
            for (int i = 0; i < lstModCategoria.size(); i++) {
                cboIdCategoria.addItem(lstModCategoria.get(i).getNombre());
            }
        } catch (Exception e) {
            System.err.println("Error al listar categoria: "+e);
        }
    }
    private void listarEstado(){
        cboIdEstado.removeAllItems();
        EstadoDao estDao = new EstadoDao();
        try {
            lstModEstado=estDao.listarEstado();
            for (int i = 0; i < lstModEstado.size(); i++) {
                cboIdEstado.addItem(lstModEstado.get(i).getNombre());
            }
        } catch (Exception e) {
            System.out.println("Error al listar estado: "+e);
        }
    }
    private void listarTipoCliente(){
        cboIdTipoCliente.removeAllItems();;
        lstModTipoCliente=clieDao.listarTipoCliente();
        for (int i = 0; i < lstModTipoCliente.size(); i++) {
            cboIdTipoCliente.addItem(lstModTipoCliente.get(i).getNombre());
        }
    }
