    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        try {
            int res = JOptionPane.showConfirmDialog(this, "¿Seguro quiere modificar los datos del cliente?","Confirmacion",JOptionPane.YES_OPTION);
            for (int i = 0; i < lstModCategoria.size(); i++) {
                if (lstModCategoria.get(i).getNombre().equals(cboIdCategoria.getSelectedItem())) {
                    int idCategoria = lstModCategoria.get(i).getId_categoria();
                    modCliente.setId_categoria(idCategoria);
                }
            }
            for (int i = 0; i < lstModEstado.size(); i++) {
                if (lstModEstado.get(i).getNombre().equals(cboIdEstado.getSelectedItem())) {
                    int idEstado = lstModEstado.get(i).getId_estado();
                    modCliente.setId_estado(idEstado);
                }
            }
            for (int i = 0; i < lstModTipoCliente.size(); i++) {
                if (lstModTipoCliente.get(i).getNombre().equals(cboIdTipoCliente.getSelectedItem())) {
                    int idTipoCliente = lstModTipoCliente.get(i).getTipoClientes();
                    modCliente.setId_tipocliente(idTipoCliente);
                }
            }
            modCliente.setEmail(txtEmail.getText());
            modCliente.setId_cliente(Integer.valueOf(txtIdCliente.getText()));
            if (res ==0) {
                clieDao.modificar(modCliente);
                JOptionPane.showMessageDialog(null, "¡Modificacion con exito!");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error en la operacion");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

