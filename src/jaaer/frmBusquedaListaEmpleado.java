    private void llenarTabla() {
        tblModelEmpleado = (DefaultTableModel) tblEmpleado.getModel();
        Object[] column = new Object[tblModelEmpleado.getColumnCount()];
        
        try {
            
            lstModEmpleado = empDao.listarTodoEmpleado();
            int tamano = lstModEmpleado.size();

            for (int i = 0; i < tamano; i++) {
                column[0] = lstModEmpleado.get(i).getIdEmpleado();
                column[1] = lstModEmpleado.get(i).getDpiEmpleado();
                column[2] = lstModEmpleado.get(i).getNombreEmpleado();
                column[3] = lstModEmpleado.get(i).getNit();
                column[4] = lstModEmpleado.get(i).getPuesto();
                column[5] = lstModEmpleado.get(i).getSueldo();
                column[6] = lstModEmpleado.get(i).getDireccion();
                
                tblModelEmpleado.addRow(column);
            }

        } catch (Exception e) {
            System.out.println("Error al llenar la tabla: "+e);
        }
    }

