    private void llenarTabla() {
        tblModelContra = (DefaultTableModel) tblContrato.getModel();
        Object[] column = new Object[tblModelContra.getColumnCount()];
        
        try {
            lstModContra = contraDao.listarTodaContratacion();
            int tamano = lstModContra.size();

            for (int i = 0; i < tamano; i++) {
                column[0] = lstModContra.get(i).getIdContratacion();
                column[1] = lstModContra.get(i).getPaquete();
                column[2] = lstModContra.get(i).getFechaContratacion();
                column[3] = lstModContra.get(i).getFechaCulminacion();
                column[4] = lstModContra.get(i).getFechaRenovacion();
                column[5] = lstModContra.get(i).getCliente();
                column[6] = lstModContra.get(i).getDireccion();
                column[7] = lstModContra.get(i).getMunicipio();
                column[8] = lstModContra.get(i).getIdEmpleado();
                tblModelContra.addRow(column);
            }
        } catch (Exception e) {
        }
    }

